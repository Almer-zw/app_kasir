private void datatable() {
    DefaultTableModel tbl = new DefaultTableModel();
    tbl.addColumn("ID Transaksi");
    tbl.addColumn("ID Pesanan");
    tbl.addColumn("Nama Menu");
    tbl.addColumn("Harga");
    tbl.addColumn("Jumlah");
    tbl.addColumn("Total");
    tbl.addColumn("Bayar");
    tblTransaksi.setModel(tbl);
    try {
        Statement statement = (Statement)
        koneksi.GetConnection().createStatement();
        ResultSet res = statement.executeQuery
        ("select * from tra");
          while (res.next())
          {
              tbl.addRow(new Object[]{
              res.getString("Idtransaksi"),
              res.getString("Idpesanan"),
              res.getString("Namamenu"),
              res.getString("Harga"),
              res.getString("Jumlah"),
              res.getString("Total"),
              res.getString("Bayar"),
          });
              tblTransaksi.setModel(tbl);
          }
    }catch (Exception e){
    JOptionPane.showMessageDialog(rootPane,"Salah");
    }
}
