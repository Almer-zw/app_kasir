    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        if(btnTambah.getText() == "TAMBAH") {
            buka();
            bersihkan();
            btnTambah.setText("SIMPAN");
            btnHapus.setEnabled(false);
            btnKeuar.setEnabled(false);
        }else {
            String Idtransaksi = txtIdTransaksi.getText();
            String Idpesanan =  (String) cmbIdPesanan.getSelectedItem();
            String Namamenu = txtNamaMenu.getText();
            String Harga = txtHarga.getText();
            String Jumlah = txtJumlah.getText();
            String Total = txtTotal.getText();
            String Bayar = txtBayar.getText();
            try {
                Statement statement = (Statement) koneksi.GetConnection().createStatement();
                statement.executeUpdate("insert into tra values('"+Idtransaksi+"','"+Idpesanan+"','"+Namamenu+"','"+Harga+"','"+Jumlah+"','"+Total+"','"+Bayar+"');");
                statement.close();
                JOptionPane.showMessageDialog(null,"Sip Data Berhasil di Simpan");
                datatable();
                bersihkan();
                sembunyikan();
                btnTambah.setText("TAMBAH");
                btnHapus.setEnabled(true);
                btnKeuar.setEnabled(true);
            }catch (Exception t){
                JOptionPane.showMessageDialog(null,"Yah Data Gagal Di Simpan Masbro");
            }
        }
    }//GEN-LAST:event_btnTambahActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        String Idtransaksi = txtIdTransaksi.getText();
        try {
            Statement statement = (Statement) koneksi.GetConnection().createStatement();
            statement.executeUpdate("delete from tra where Idtransaksi = ('"+Idtransaksi+"');");
            bersihkan();
            sembunyikan();
            datatable();
            JOptionPane.showMessageDialog(null,"Sip Data Berhasil dihapus");
        }catch (Exception t){
            JOptionPane.showMessageDialog(null,"Yah Data Gagal Di Simpan Masbro");
        }
    }//GEN-LAST:event_btnHapusActionPerformed

